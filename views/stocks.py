def show_stock_details(stock):
    """Wy≈õwietla szczeg√≥≈Çy akcji."""
    
    st.markdown(f"#### üìà {stock['symbol']} - Szczeg√≥≈Çy")
    
    # Podstawowe metryki
    total_cost = stock['quantity'] * stock['avg_price_usd']
    current_value = stock['quantity'] * stock['current_price_usd']
    unrealized_gain = current_value - total_cost
    return_pct = (unrealized_gain / total_cost * 100) if total_cost > 0 else 0
    
    col1, col2 = st.columns(2)
    
    with col1:
        st.metric("üíº Ilo≈õƒá akcji", stock['quantity'])
        st.metric("üí∞ ≈örednia cena", format_currency(stock['avg_price_usd']))
        st.metric("üíµ Koszt ca≈Çkowity", format_currency(total_cost))
    
    with col2:
        st.metric("üìä Aktualna cena", format_currency(stock['current_price_usd']))
        st.metric("üíé Warto≈õƒá aktualna", format_currency(current_value))
        
        gain_text, gain_color = format_gain_loss(unrealized_gain)
        st.metric("üìà Zysk/Strata", gain_text, delta=f"{return_pct:.1f}%")
    
    # Dodatkowe informacje z API
    with st.spinner("Pobieranie dodatkowych informacji..."):
        stock_info = pricing_service.get_stock_info(stock['symbol'])
        
        if stock_info:
            st.markdown("##### üìã Informacje dodatkowe")
            
            info_col1, info_col2 = st.columns(2)
            
            with info_col1:
                if stock_info.get('sector'):
                    st.text(f"üè¢ Sektor: {stock_info['sector']}")
                if stock_info.get('industry'):
                    st.text(f"üè≠ Bran≈ºa: {stock_info['industry']}")
                if stock_info.get('marketCap'):
                    st.text(f"üí∞ Kapitalizacja: {format_currency(stock_info['marketCap'])}")
            
            with info_col2:
                if stock_info.get('dividendYield'):
                    st.text(f"üíé Dywidenda: {format_percentage(stock_info['dividendYield'] * 100)}")
                if stock_info.get('trailingPE'):
                    st.text(f"üìä P/E: {stock_info['trailingPE']:.2f}")
                if stock_info.get('beta'):
                    st.text(f"üìà Beta: {stock_info['beta']:.2f}")

def show_stock_chart(symbol):
    """Wy≈õwietla wykres historyczny akcji."""
    
    st.markdown(f"#### üìä Wykres - {symbol}")
    
    period = st.selectbox(
        "Okres",
        ["1mo", "3mo", "6mo", "1y", "2y"],
        format_func=lambda x: {
            "1mo": "1 miesiƒÖc",
            "3mo": "3 miesiƒÖce", 
            "6mo": "6 miesiƒôcy",
            "1y": "1 rok",
            "2y": "2 lata"
        }[x]
    )
    
    with st.spinner("Pobieranie danych historycznych..."):
        hist_data = pricing_service.get_historical_data(symbol, period)
        
        if hist_data:
            df = pd.DataFrame({
                'Date': pd.to_datetime(hist_data['dates']),
                'Close': hist_data['close'],
                'Volume': hist_data['volume']
            })
            
            # Wykres ceny
            fig = go.Figure()
            
            fig.add_trace(go.Scatter(
                x=df['Date'],
                y=df['Close'],
                mode='lines',
                name='Cena zamkniƒôcia',
                line=dict(color='blue', width=2)
            ))
            
            fig.update_layout(
                title=f"Historia cen {symbol}",
                xaxis_title="Data",
                yaxis_title="Cena (USD)",
                height=400
            )
            
            st.plotly_chart(fig, use_container_width=True)
        
        else:
            st.error("Nie mo≈ºna pobraƒá danych historycznych.")